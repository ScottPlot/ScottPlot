namespace ScottPlot;

/// <summary>
/// Describes the direction a <see cref="AxisGradientColorPosition"/> is using for its position
/// </summary>
public enum AxisGradientDirection
{
    /// <summary>
    /// Assumes <see cref="AxisGradientColorPosition"/> positions are on the X axis
    /// </summary>
    Horizontal,

    /// <summary>
    /// Assumes <see cref="AxisGradientColorPosition"/> positions are on the Y axis
    /// </summary>
    Vertical,
}
